@login_required(login_url='doctorlogin')
@user_passes_test(is_doctor)
def doctor_dashboard_view(request):
    patientcount=models.Patient.objects.all().filter(assignedDoctorId=request.user.id).count()
    appointmentcount=models.Appointment.objects.all().filter(doctorId=request.user.id).count()
    appointments=models.Appointment.objects.all().filter(doctorId=request.user.id).order_by('-id')
    patientid=[]
    for a in appointments:
        patientid.append(a.patientId)
    patients=models.Patient.objects.all().filter(user_id__in=patientid).order_by('-id')
    appointments=zip(appointments,patients)
    mydict={
    'patientcount':patientcount,
    'appointmentcount':appointmentcount,
    'appointments':appointments,
    'doctor':models.Doctor.objects.get(user_id=request.user.id),
    }
    return render(request,'hospital/doctor_dashboard.html',context=mydict)


